public class MultipleMapIdsException extends Exception{

    public MultipleMapIdsException() {
        super();
    }

    public MultipleMapIdsException(String message) {
        super(message);
    }

    public MultipleMapIdsException(String message, Throwable cause) {
        super(message, cause);
    }

    public MultipleMapIdsException(Throwable cause) {
        super(cause);
    }
}
